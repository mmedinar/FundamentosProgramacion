
def suma(num1: int, num2: int) -> int:
    result = num1 + num2
    return result


def resta(num1: int, num2: int) -> int:
    result = num1 - num2
    return result


def multiplicacion(num1: int, num2: int) -> int:
    result = num1 * num2
    return result

def division(num1: int, num2: int) -> float:
    result = num1 / num2
    return result